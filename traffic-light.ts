let color: string = "yellow";

switch (color) {
  case "red":
    console.log("The Next Color Will be green");
    color = "green";
    break;
  case "yellow":
    console.log("The Next Color Will be red");
    color = "red";
    break;
  case "green":
    console.log("The Next Color Will be yellow");
    color = "yellow";
    break;
    default:
}
